    private String getEscala(String substring) {
        String res = "";
        switch (substring) {
            case "EXCELENTE ":
                res = "E";
                break;
            case "SATISFACTORIO":
                res = "S";
                break;
            case "MEDIANAMENTE SATISFACTORIO":
                res = "MS";
                break;
        }
        return res;
    }
