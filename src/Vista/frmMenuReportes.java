    private void validacionSoloNumerosYGuion(KeyEvent evt, int maximo, int lim) {
        if (coordinador.validacionLimiteSoloNumerosYGuion(evt.getKeyChar() + "")) {
            if (lim >= maximo) {
                evt.consume();
                getToolkit().beep();
            }
        } else {
            evt.consume();
            getToolkit().beep();
        }
    }

    private String getEscala(String substring) {
        String res = "";
        switch (substring) {
            case "EXCELENTE ":
                res = "E";
                break;
            case "SATISFACTORIO":
                res = "S";
                break;
            case "MEDIANAMENTE SATISFACTORIO":
                res = "MS";
                break;
        }
        return res;
    }
    
